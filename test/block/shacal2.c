#include <stdint.h>
#include <stdio.h>

#include <kripto/block.h>
#include <kripto/block/shacal2.h>

int main(void)
{
	const uint8_t k[64] =
	{
		0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
		0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F,
		0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17,
		0x18, 0x19, 0x1A, 0x1B, 0x1C, 0x1D, 0x1E, 0x1F,
		0x20, 0x21, 0x22, 0x23, 0x24, 0x25, 0x26, 0x27,
		0x28, 0x29, 0x2A, 0x2B, 0x2C, 0x2D, 0x2E, 0x2F,
		0x30, 0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37,
		0x38, 0x39, 0x3A, 0x3B, 0x3C, 0x3D, 0x3E, 0x3F
	};
	const uint8_t pt[32] =
	{
		0x98, 0xBC, 0xC1, 0x04, 0x05, 0xAB, 0x0B, 0xFC,
		0x68, 0x6B, 0xEC, 0xEC, 0xAA, 0xD0, 0x1A, 0xC1,
		0x9B, 0x45, 0x25, 0x11, 0xBC, 0xEB, 0x9C, 0xB0,
		0x94, 0xF9, 0x05, 0xC5, 0x1C, 0xA4, 0x54, 0x30
	};
	const uint8_t ct[32] =
	{
		0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77,
		0x88, 0x99, 0xAA, 0xBB, 0xCC, 0xDD, 0xEE, 0xFF,
		0x10, 0x21, 0x32, 0x43, 0x54, 0x65, 0x76, 0x87,
		0x98, 0xA9, 0xBA, 0xCB, 0xDC, 0xED, 0xFE, 0x0F
	};
	kripto_block *s;
	unsigned int i;
	uint8_t t[32];

	puts("kripto_block_shacal2");

	s = kripto_block_create(kripto_block_shacal2, 0, k, 64);
	if(!s) puts("error");

	kripto_block_encrypt(s, pt, t);
	for(i = 0; i < 32; i++) if(t[i] != ct[i])
	{
		printf("Encrypt: FAIL\n");
		break;
	}
	if(i == 32) printf("Encrypt: OK\n");

	kripto_block_decrypt(s, ct, t);
	for(i = 0; i < 32; i++) if(t[i] != pt[i])
	{
		printf("Decrypt: FAIL\n");
		break;
	}
	if(i == 32) printf("Decrypt: OK\n");

	kripto_block_destroy(s);

	return 0;
}
