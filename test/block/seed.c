	c = clock();
	for(i = 0; i < ITERATIONS; i++) kripto_block_seed_encrypt(s, t, t);
	c = clock() - c;

	printf("encrypt: %.1f cycles/byte, %.1f MB/s\n",
		(float)c / (float)(ITERATIONS * 16) * _CPU,
		(float)(ITERATIONS * 16) / ((float)c / (float)CLOCKS_PER_SEC) / 1000000.0);

	c = clock();
	for(i = 0; i < ITERATIONS; i++) kripto_block_seed_decrypt(s, t, t);
	c = clock() - c;

	printf("decrypt: %.1f cycles/byte, %.1f MB/s\n",
		(float)c / (float)(ITERATIONS * 16) * _CPU,
		(float)(ITERATIONS * 16) / ((float)c / (float)CLOCKS_PER_SEC) / 1000000.0);

	kripto_block_seed_delete(s);
	#endif

	return(0);
