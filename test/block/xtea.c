#include <stdint.h>
#include <stdio.h>

#include <kripto/block/xtea.h>

int main(void)
{
	kripto_block *s;
	unsigned int i;
	unsigned int n;
	uint8_t t[8] =
	{
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00
	};
	const uint8_t k[16] =
	{
		0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
		0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F
	};
	const uint8_t pt[8] =
	{
		0x41, 0x42, 0x43, 0x44, 0x45, 0x46, 0x47, 0x48
	};
	const uint8_t ct[16][8] =
	{
		{0xA0, 0x39, 0x05, 0x89, 0xF8, 0xB8, 0xEF, 0xA5},
		{0xCB, 0x92, 0x6F, 0x96, 0xD7, 0xE4, 0xA2, 0xF8},
		{0x27, 0x7C, 0x1B, 0x95, 0xFE, 0x2F, 0x2D, 0x66},
		{0x73, 0x1B, 0xB8, 0x46, 0x45, 0x29, 0x9E, 0x65},
		{0x5E, 0x3B, 0x4F, 0x00, 0xF4, 0x96, 0xCD, 0xCC},
		{0x02, 0xA3, 0x77, 0x82, 0x07, 0x2C, 0x31, 0x42},
		{0xB7, 0x2E, 0x56, 0x2F, 0xB6, 0xF4, 0x71, 0xDB},
		{0xC0, 0x36, 0x07, 0x02, 0x4A, 0xD8, 0xE4, 0x24},
		{0x97, 0x99, 0x48, 0x4A, 0xDA, 0x8F, 0xC2, 0x56},
		{0x10, 0x87, 0xC2, 0x02, 0x67, 0xEE, 0xDB, 0xD9},
		{0x26, 0x91, 0x77, 0x94, 0xC6, 0x7A, 0x03, 0x40},
		{0x51, 0xEA, 0x6A, 0x4D, 0x09, 0x68, 0x79, 0x0F},
		{0x64, 0x31, 0x01, 0x5D, 0x0E, 0xAB, 0x23, 0xE7},
		{0x5D, 0x55, 0xA9, 0x9C, 0x42, 0x7D, 0x3B, 0x67},
		{0xFB, 0x47, 0x68, 0xC6, 0xDA, 0x39, 0x2A, 0x4F},
		{0x49, 0x7D, 0xF3, 0xD0, 0x72, 0x61, 0x2C, 0xB5}
	};

	puts("kripto_block_xtea");

	for(n = 1; n <= 16; n++)
	{
		s = kripto_block_create(kripto_block_xtea, k, n, 0);
		if(!s) puts("error");

		kripto_block_encrypt(s, pt, t);
		for(i = 0; i < 8; i++) if(t[i] != ct[n - 1][i])
		{
			printf("%u-bit key encrypt: FAIL\n", n * 8);
			break;
		}
		if(i == 8) printf("%u-bit key encrypt: OK\n", n * 8);
		kripto_block_decrypt(s, ct[n - 1], t);
		for(i = 0; i < 8; i++) if(t[i] != pt[i])
		{
			printf("%u-bit key decrypt: FAIL\n", n * 8);
			break;
		}
		if(i == 8) printf("%u-bit key decrypt: OK\n", n * 8);

		kripto_block_destroy(s);
	}

	return 0;
}
