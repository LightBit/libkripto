static uint32_t F(uint32_t x)
{
	x = S76[(uint8_t)(x >> 24)] << 24
		| S54[(uint8_t)(x >> 16)] << 16
		| S32[(uint8_t)(x >> 8)] << 8
		| S10[(uint8_t)x];

	return ROL32(x, 11);
}

static void gost_encrypt
(
	const kripto_block *s,
	const void *pt,
	void *ct
)
{
	uint32_t l;
	uint32_t r;
	unsigned int i;

	l = U8TO32_LE(CU8(pt));
	r = U8TO32_LE(CU8(pt) + 4);

	for(i = 0; i < s->rounds;)
	{
		r ^= F(l + s->k[i++]);
		l ^= F(r + s->k[i++]);
	}

	U32TO8_LE(r, U8(ct));
	U32TO8_LE(l, U8(ct) + 4);
}

static void gost_decrypt
(
	const kripto_block *s,
	const void *ct,
	void *pt
)
{
	uint32_t l;
	uint32_t r;
	unsigned int i;

	l = U8TO32_LE(CU8(ct));
	r = U8TO32_LE(CU8(ct) + 4);

	for(i = s->rounds; i;)
	{
		r ^= F(l + s->k[--i]);
		l ^= F(r + s->k[--i]);
	}

	U32TO8_LE(r, U8(pt));
	U32TO8_LE(l, U8(pt) + 4);
}

static void gost_setup
(
	kripto_block *s,
	const uint8_t *key,
	unsigned int key_len
)
{
	unsigned int i;
	unsigned int j;

	for(i = 0; i < (key_len + 3) >> 2; i++)
		s->k[i] = 0;

	for(i = key_len - 1; i != UINT_MAX; i--)
		s->k[i >> 2] = (s->k[i >> 2] << 8) | key[i];

	key_len = (key_len + 3) >> 2;

	for(i = key_len, j = 0; s->rounds - i > key_len; i++)
	{
		s->k[i] = s->k[j];

		if(++j == key_len) j = 0;
	}

	/* reversed */
	for(j = key_len - 1; j != UINT_MAX; i++, j--)
		s->k[i] = s->k[j];

}

static kripto_block *gost_create
(
	const void *key,
	unsigned int key_len,
	unsigned int r
)
{
	kripto_block *s;

	if(!r) r = 32;

	s = malloc(sizeof(kripto_block) + (r << 2));
	if(!s) return 0;

	s->desc = kripto_block_gost;
	s->size = sizeof(kripto_block) + (r << 2);
	s->k = (uint32_t *)(((uint8_t *)s) + sizeof(kripto_block));
	s->rounds = r;

	gost_setup(s, key, key_len);

	return s;
}

static void gost_destroy(kripto_block *s)
{
	kripto_memwipe(s, s->size);
	free(s);
}

static kripto_block *gost_change
(
	kripto_block *s,
	const void *key,
	unsigned int key_len,
	unsigned int r
)
{
	if(!r) r = 32;

	if(sizeof(kripto_block) + (r << 2) > s->size)
	{
		gost_destroy(s);
		s = gost_create(key, key_len, r);
	}
	else
	{
		s->rounds = r;
		gost_setup(s, key, key_len);
	}

	return s;
}

static const struct kripto_block_desc gost =
{
	&gost_encrypt,
	&gost_decrypt,
	&gost_create,
	&gost_change,
	&gost_destroy,
	8, /* block size */
	32 /* max key */
};

kripto_block_desc *const kripto_block_gost = &gost;
