#include <stddef.h>
#include <stdint.h>

#include <kripto/memwipe.h>

void kripto_memwipe(void *dst, size_t len)
{
	volatile uint8_t *x = (volatile uint8_t *)dst;

	while(len--) *x++ = '\0';
}
